async function debugWaterMask(url: URL, env: Env): Promise<Response> {
  const z = Number(url.searchParams.get("z") ?? "10");
  const x = Number(url.searchParams.get("x") ?? "1051");
  const y = Number(url.searchParams.get("y") ?? "809");
  const tilesetName = url.searchParams.get("tileset");
  const tileset = resolveTileset(tilesetName ?? undefined);
  if (!tileset) return notFound(`unknown tileset: ${tilesetName}`);
  const bounds = geodeticTileBounds(z, x, y);
  const wm = await resolveWatermask(tileset);
  if (!wm) return json({ error: "watermask disabled for tileset", tileset: tileset.name });

  const t0 = Date.now();
  const mask = await wm.provider.buildMask(bounds);
  const ms = Date.now() - t0;

  let waterCells = 0;
  let landCells = 0;
  let kind: "uniform-land" | "uniform-water" | "grid" = "grid";
  if (mask.length === 1) {
    kind = mask[0] === 255 ? "uniform-water" : "uniform-land";
  } else {
    for (const v of mask) {
      if (v === 255) waterCells++;
      else landCells++;
    }
  }
  return json({
    z, x, y, bounds, ms,
    source: { url: wm.url, version: wm.version },
    kind,
    maskBytes: mask.length,
    ...(kind === "grid"
      ? { waterCells, landCells, waterFraction: waterCells / (waterCells + landCells) }
      : {}),
  });
}

/**
 * Snapshot every live tileset's currently-resolved version into the
 * `CleanupTileset` shape the sweep expects. Keeps the cron path and the
 * debug endpoint pointing at the same versions and centralizes the
 * `Tileset → CleanupTileset` projection.
 */
function resolveLiveCleanupTilesets(): Record<string, { name: string; version: string }> {
  const out: Record<string, { name: string; version: string }> = {};
  for (const [key, t] of Object.entries(TILESETS)) {
    out[key] = { name: t.name, version: resolveTilesetVersion(t) };
  }
  return out;
}

async function debugCleanup(url: URL, env: Env): Promise<Response> {
  if (!env.R2) return json({ error: "R2 binding missing" }, { status: 500 });
  const dryRun = url.searchParams.get("dryRun") !== "0";
  const maxDeletes = Number(url.searchParams.get("maxDeletes") ?? "5000");
  const t0 = Date.now();
  const result = await runCleanup(env.R2, resolveLiveCleanupTilesets(), { dryRun, maxDeletes });
  return json({ ...result, ms: Date.now() - t0 });
}

function summarize(arr: Float32Array): { min: number; max: number; mean: number; valid: number } | null {
  let min = Infinity;
  let max = -Infinity;
  let sum = 0;
  let n = 0;
  for (const v of arr) {
    if (!Number.isFinite(v) || v <= -9999) continue;
    if (v < min) min = v;
    if (v > max) max = v;
    sum += v;
    n++;
  }
  return n > 0 ? { min, max, mean: sum / n, valid: n } : null;
}

function json(body: unknown, init?: ResponseInit): Response {
  return new Response(JSON.stringify(body), {
    ...init,
    headers: {
      "content-type": "application/json; charset=utf-8",
      ...(init?.headers ?? {}),
    },
  });
}

const METADATA_CACHE_CONTROL = "public, max-age=3600";

/** JSON response with strong ETag + If-None-Match support. */
async function metadataJson(req: Request, body: unknown): Promise<Response> {
  const text = JSON.stringify(body);
  const etag = await bodyEtag(text);
  if (matchesIfNoneMatch(req.headers.get("if-none-match"), etag)) {
    return new Response(null, {
      status: 304,
      headers: { etag, "cache-control": METADATA_CACHE_CONTROL },
    });
  }
  return new Response(text, {
    headers: {
      "content-type": "application/json; charset=utf-8",
      "cache-control": METADATA_CACHE_CONTROL,
      etag,
    },
  });
}

